    <h2 class="artykul">Bramkarz Śląska: Nie wiem, co myśleć o debiucie</h2>
    <p class="artykul">
        Bramkarz Śląska Wrocław Dominik Budzyński w ostatnim wywiadzie nie krył rozczarowania z porażki w swoim debiucie w rozgrywkach LOTTO Ekstraklasy.

        - Czy cieszę się z debiutu? Totalnie nie wiem co o tym myśleć. Przegraliśmy wysoko mecz, ale z drugiej strony spotkało mnie coś fajnego, bo zadebiutowałem w meczu LOTTO Ekstraklasy. Na pewno wolałbym zagrać w zwycięskim spotkaniu - powiedział golkiper wrocławkiego zespołu, cytowany przez oficjalną klubową stronę internetową.

        - Od razu po wejściu na boisku musiałem zmierzyć się ze strzałem Vassiljeva ze stałego fragmentu. Wyczekałem go do końca, ale zabrakło mi asekuracji. Sparowałem mocno do boku, a tam rywal dobił. W końcówce nie byliśmy sobą. Wracamy w złych humorach, ale musimy myśleć już o kolejnym meczu - dodał.

        - Duża grupa kibiców przyjechała z Wrocławia, za co im dziękujemy. Na pewno trochę ich zawiedliśmy, ale mam nadzieję, że w następnym spotkaniu odbudujemy zaufanie - zakończył.
    </p>
