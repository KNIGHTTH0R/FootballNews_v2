    <h2 class="artykul">Oezil nie zagra przeciwko Bayernowi</h2>
    <p class="artykul">
        Mesut Oezil nie zagra w rewanżowym meczu 1/8 finału Ligi Mistrzów Arsenalu Londyn z Bayernem Monachium. Niemiec trenował w poniedziałek z drużyną, ale przeciwko Bawarczykom nie wystąpi.

        Oezila zabrakło w składzie „Kanonierów” na sobotni mecz wyjazdowym Premier League przegrany z Liverpoolem 1:3. Niemiecki pomocnik był chory i choć w poniedziałek już trenował, to przeciwko Bayernowi na boisku go nie zobaczymy.

        - Był chory i przez cały poprzedni tydzień nie trenował. Nie sądzę, aby był gotowy pod względem fizycznym, aby znaleźć się w kadrze meczowej. Może w najbliższy weekend będzie w składzie - powiedział Wenger, cytowany przez „BBC”.

        Drużyna z północnego Londynu w pierwszym spotkaniu przegrała w Monachium aż 1:5. Początek rewanżowego starcia na Emirates we wtorek o godzinie 20.45.
