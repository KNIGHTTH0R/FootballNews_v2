    <h2 class="artykul">Hamsik: Real może nas zranić w każdym momencie</h2>
    <p class="artykul">
        SSC Napoli zagra u siebie we wtorek z Realem Madryt w rewanżowym spotkaniu 1/8 finału Ligi Mistrzów. Marek
        Hamsik, kapitan drużyny z Serie A, powiedział, że całe miasto czeka na początek meczu z „Królewskimi”.

        W pierwszym spotkaniu jego podopieczni prowadzili w stolicy Hiszpanii 1:0, ale ostatecznie przegrali 1:3. Teraz
        do awansu potrzebują dwubramkowego zwycięstwa.

        - Musimy wyjść na murawę i atakować, ponieważ to jest coś, co robimy cały czas. Zwykle u siebie udaje nam się
        rozgrywać dobre mecze i jestem pewien, że stać nas na taki również przeciwko Realowi Madryt - powiedział Hamsik,
        cytowany przez oficjalną stronę internetową UEFA.

        - Oni są bardzo niebezpieczni w ofensywie. Mogą nas zranić w każdym momencie. Będziemy musieli dobrze bronić i
        wykorzystać naszą pewność siebie oraz taktykę, aby sprawić im kłopoty, kiedy będziemy mogli to zrobić - dodał
        słowacki pomocnik.

        - To jest wielki mecz. Nie musimy czuć presji, po prostu trzeba cieszyć się chwilą, ponieważ zagramy ze
        wspaniałą drużyną, która ma skład pełen mistrzów. Zmierzymy się z najbardziej utytułowaną drużyną na świecie,
        ale to powinno sprawić, że będziemy mieli jeszcze więcej energii. Całe miasto czeka na ten moment, kibice dadzą
        nam ogromne wsparcie. Myślę, że nasz hymn będzie można usłyszeć w Turynie - stwierdził.

        Początek spotkania na Stadio San Paolo we wtorek o godzinie 20.45.
    </p>
