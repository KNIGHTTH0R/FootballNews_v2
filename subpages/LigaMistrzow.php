    <h2 class="artykul">Typujemy Europejskie Puchary. Czy Barcelona odrobi straty i awansuje?</h2>
    <p class="artykul">
        Już bez Legii, ale wciąż z Polakami w grze. Liga Mistrzów i Liga Europy wkraczają w decydujące fazy. W grze
        zostało po 16 najlepszych ekip w obu rozgrywkach. Wyciągamy magiczną kulę i wróżymy, co może się wydarzyć w
        najbliższych spotkaniach.
        Napoli - Real Madryt (pierwszy mecz: 1:3)
        wtorek, 20.45

        W pierwszym spotkaniu neapolitańczycy lekko “ukąsili” ekipę Realu, ale cały mecz pokazał, że gospodarzom rewanżu
        będzie raczej trudno awansować. Na dodatek wciąż nie w pełni sił jest Arkadiusz Milik, którego drużyna z San
        Paolo mogłaby wykorzystać jako swoją tajną broń.

        Nasze przewidywania: “Królewscy” będą spokojnie kontrolować mecz i wygrają jedną bramką.

        Arsenal FC - Bayern Monachium (pierwszy mecz: 1:5)
        wtorek, 20.45

        “Kanonierzy” mieli swoje szanse w Monachium i gdyby wykorzystali nadarzające się okazje, z innymi nastrojami
        wracaliby do Londynu. A tak, do odrobienia pozostała czterobramkowa strata i nawet gol strzelony na wyjeździe
        nie daje zbyt wielkich nadziei na sukces.

        Nasze przewidywania: Arsenal powalczy, by pokazać, że wciąż należy do topowych drużyn. Może nawet wygra, by
        przynajmniej częściowo zmazać plamę za pierwszy mecz… choć remis jest bardziej prawdopodobny.
