    <h2 class="artykul">Nieuznany gol, karny z kapelusza? Szef polskich sędziów broni arbitrów</h2>
    <p class="artykul">
        Trudna kolejka dla sędziów, ale ogólnie dobrze pracowali - tak Przewodniczący Kolegium Sędziów PZPN Zbigniew
        Przesmycki podsumował zachowanie arbitrów w trakcie 24. kolejki piłkarskiej Ekstraklasy.

        To był mocno kontrowersyjny weekend na polskich boiskach. Jedną z największych wpadek zaliczył sędzia spotkania
        Cracovii z Arką. Starcie zakończyło się remisem, a Paweł Gil nie uznał prawidłowo zdobytego gola dla gospodarzy.
        Powtórki telewizyjne pokazały, że piłka spadła za linię bramkową.
        Przesmycki broni jednak swoich podopiecznych: - To ciężka sytuacja do zauważenia. Dla sędziego asystenta piłka
        była między słupkami - powiedział, cytowany przez Polską Agencję Prasową. Tłumaczył, że nawet piłkarze w tym
        wypadku nie protestowali.

        I dodaje, że w takim momencie bardzo przydałaby się technologia "goal-line".</p>
    <p class="artykul"> Przesmycki jest jak najbardziej za, ale zdaje sobie sprawę, że wysokie koszty takiego
        rozwiązania mogą być dla niektórych nieakceptowalne.

        Przewodniczący bronił także sędziego meczu Korony Kielce z Górnikiem Łęczna. W samej końcówce arbiter podyktował
        rzut karny dla gospodarzy, zdaniem wielu - niesłuszny.

        Co na to Przesmycki? Uważa, że gracz Górnika nie był zainteresowany piłką, ale przyznaje, że: - Zawodnik
        gospodarzy trochę nieudolnie upadł na murawę, wyrzucając ręce w górę.
    </p>
