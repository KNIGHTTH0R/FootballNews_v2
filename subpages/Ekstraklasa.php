    <h2 class="artykul">Piłkarze Lecha będą gotowi na Arkę</h2>
    <p class="artykul">
        Żaden z piłkarzy nie odczuwa poważnych dolegliwości po niedzielnym meczu z Lechią. Wszyscy zawodnicy powinni być
        do dyspozycji sztabu szkoleniowego na zbliżające się starcie z Arką - czytamy na oficjalnej stronie klubowej.
        Najbardziej poszkodowany po końcowym gwizdku był Maciej Makuszewski. Skrzydłowy Kolejorza ma stłuczony bark. W
        poniedziałek trenował indywidualnie wspólnie z fizjoterapeutami. - We wtorek weźmie udział w treningu z całym
        zespołem - uspokaja sytuację szef sztabu medycznego Lecha Poznań dr Krzysztof Pawlaczyk.

        Indywidualnie w poniedziałek trenował też Elvis Kokalović. Chorwat cały czas czeka nie tylko na debiut, ale też
        zakwalifikowanie się do meczowej osiemnastki. - Indywidualne treningi przygotowują go do mocnych obciążeń oraz
        gry na wysokim poziomie. Cały czas nadrabia zaległości treningowe - przyznaje klubowy lekarz.

        W meczu z Arką nie będzie mogło zagrać tylko dwóch piłkarzy. To Tomasz Kędziora i Jan Bednarek. Obaj podczas
        tego meczu będą pauzowali za żółte kartki obejrzane w trakcie starcia z Lechią.
    </p>
