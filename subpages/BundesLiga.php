    <h2 class="artykul">Kicker: Słaby Lewandowski, fatalny Olkowski</h2>
    <p class="artykul">
        Koeln uległo u siebie Bayernowi Monachium 0:3 (0:2) w spotkaniu 23. kolejki niemieckiej Bundesligi. Na
        RheinEnergieStadion w podstawowym składzie gospodarzy znalazł się Paweł Olkowski, natomiast wśród gości Robert
        Lewandowski. Reprezentanci Polski zaliczyli pełne zawody, ale obaj - zdaniem niemieckich mediów - nie wypadli
        najlepiej.

        Lewandowskiego dziennikarze "Kickera" ocenili na 3,5 (w skali 1-6, im mniej, tym lepiej). Napastnik był dobrze
        pilnowany i zaledwie raz uderzył na bramkę rywala. W Bayernie tylko jeden zawodnik otrzymał niższą notę (był to
        Douglas Costa).

        Fatalnie wypadł za to Olkowski, który zapracował zaledwie na "5", żaden z graczy Koeln nie otrzymał niższej
        oceny, a tylko jeden otrzymał równie słabą.

        Co z innymi Polakami? W tej kolejce zagrał Jakub Błaszczykowski (w drugim kolejnym meczu zaliczył pełne 90
        minut), któremu wystawiono solidną "tróję". Z taką samą notą swój mecz kończył Łukasz Piszczek (Borussia
        rozgromiła Bayer 6:2).
    </p>
