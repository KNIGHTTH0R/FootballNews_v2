    <h2 class="artykul">Roger Schmidt zwolniony z Bayeru Leverkusen</h2>
    <p class="artykul">
        Roger Schmidt nie jest już trenerem Bayeru Leverkusen. Niemiecki trener został zwolniony po przegranym meczu z
        Borussią Dortmund 2:6.

        Roger Schmidt objął Bayer latem 2014 roku. W pierwszym sezonie pod jego wodzą zespół zakończył rozgrywki
        Bundesligi na czwartym miejscu, a w kolejnym na trzecim. W obecnym sezonie Bayer radzi sobie jednak gorzej. Po
        23 meczach zajmuje 9. lokatę. W Lidze Mistrzów natomiast niemiecki zespół jest bliski odpadnięcia z rozgrywek. W
        pierwszym meczu 1/8 finału przegrał u siebie z Atletico Madryt 2:4.

        Wczoraj Aptekarze przegrali w lidze bardzo wysoko z Borussią Dortmund 2:6 co przesądziło o losie szkoleniowca.

        Nazwisko nowego trenera Bayeru Leverkusen poznamy w najbliższym czasie.
    </p>
